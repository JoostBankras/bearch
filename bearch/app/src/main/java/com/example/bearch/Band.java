package com.example.bearch;

public class Band {
    public String name;
    public String description;
    public String location;
    public String genre;
    public String imageURI;

    public Band(String Name, String Description, String Location, String Genre, String ImageURI){
        name = Name;
        description = Description;
        location = Location;
        genre = Genre;
        imageURI = ImageURI;
    }
}
